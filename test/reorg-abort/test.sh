#!bin/bash

#------------------------------------------------------------------------------#
# Check Function
#------------------------------------------------------------------------------#
function checkEmpty(){
  if [ ! -s $FILE ]; then
    echo Success: $NAME >> log
  else
    echo Error: $NAME >> log
    RC=0
  fi
}


#------------------------------------------------------------------------------#
# Initialize
#------------------------------------------------------------------------------#
set -u
THIS_DIR=$(cd $(dirname $0); pwd)
cd $THIS_DIR

rm out* &> /dev/null
rm log &> /dev/null


#------------------------------------------------------------------------------#
# Variable
#------------------------------------------------------------------------------#
MKDR='../../mkdr.py'
RC=1


#------------------------------------------------------------------------------#
# Reorg
#------------------------------------------------------------------------------#
NAME='Reorg - Abort - Spdecified file does not exist'
python $MKDR --reorg &> out-reorg-abort
diff out-reorg-abort check/out-reorg-abort &> out-diff
FILE=out-diff
checkEmpty
rm out* &> /dev/null


#------------------------------------------------------------------------------#
# Postprocess
#------------------------------------------------------------------------------#
rm out* &> /dev/null
echo $RC

